import mongoose, { Schema } from "mongoose";

const tagScheme = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    userId: {
      type: mongoose.Schema.Types.ObjectId,
      ref: "User",
      required: true,
    },
  },
  { timestamps: true }
);

export const Tag = mongoose.model("Tag", tagScheme);
